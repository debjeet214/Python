# list in python

numbers = [1, 21, 65, "Rusell", 23, "Debjeet"]
numbers[3] = "Mitali"  # we can modify too

print(numbers)  # here we can print all the elements in the list
print(numbers[4])    # here we can access a single eleemnt from the list too
print(numbers[1:3])  # prints only a set of elements.

# we can use negative index to get value from the end of the list

print(numbers[-1])
print(numbers)
